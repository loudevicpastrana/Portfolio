// GPIO initialization for LED on PA5
void GPIO_Init(void) {
    // Enable GPIOA clock
    RCC->AHB2ENR |= RCC_AHB2ENR_GPIOAEN;

    // Set PA5 as output (for LED1)
    GPIOA->MODER &= ~(GPIO_MODER_MODE5);   // Clear previous settings
    GPIOA->MODER |= GPIO_MODER_MODE5_0;    // Set PA5 as output
    GPIOA->OTYPER &= ~GPIO_OTYPER_OT_5;    // Output push-pull
    GPIOA->OSPEEDR |= GPIO_OSPEEDR_OSPEED5; // High speed
    GPIOA->PUPDR &= ~GPIO_PUPDR_PUPD5;     // No pull-up, no pull-down
}

